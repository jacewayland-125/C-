#include <iostream>
using namespace std;

int main()
{
    int i, y = 1, a = 0;
    cout << "Inserire il valore: " << endl;
    cin >> i;
    cout << "Triangolo di Floyd: " << endl;
    for (int q = 1; q <= 1; q++)
    {
        for (int i = 0; i < y; i++)
        {
            a++;
            cout << a << " " << endl;
        }
        y++;
        cout << endl;
    }
    return 0;
}